# Data structure
# List
# define a list

food = ["Dahi Bhallay", "Biryani","Daal","Samosay","Shami","Palak paneer"]
print(food)

print(food[1])
food[1] = "chicken palao"

print(food[1])
print(food)

# 2-tuple

coordinates = (4.21, 9.29)
print(coordinates)
print(coordinates[0])

# 3-set
food_set = {"Dahi Bhallay", "Biryani","Daal","Samosay","Shami","Palak paneer"}
print(food_set)
food_set.add('pakora')
print(food_set)

# 4-Dictionary

car = {'brand':'Ford', 'model':'Mustang', 'year':1964}
print(car)
car['year'] = 2023
print(car['year'])
print(car)